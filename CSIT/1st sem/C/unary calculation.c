#include<stdio.h>
#include<conio.h>
int main()
{
    int i=5,j=10;
    printf("i=%d",i);
    printf("\ni=%d",++i);
    printf("\ni++=%d",i++);
    printf("\n++i=%d",++i);
    printf("\nj=%d",j);
    printf("\n--j=%d",--j);
    printf("\nj--=%d",j--);
    printf("\nj++=%d",j++);
    printf("\n++j=%d",++j);
    getch();
}
